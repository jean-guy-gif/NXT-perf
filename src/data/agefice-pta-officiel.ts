/**
 * Retourne tous les PTA disponibles pour un département donné.
 * Inclut la PTA 607 dématérialisée (présente dans tous les départements).
 */
export function getPTAByDepartment(departementCode: string): PTAOfficiel[] {
  if (!departementCode) return [];
  return PTA_OFFICIEL.filter((p) => p.departementCode === departementCode);
}

/**
 * Retourne la PTA 607 dématérialisée nationale (option par défaut digital).
 */
export function getPTA607Dematerialisee(): PTAOfficiel | undefined {
  return PTA_OFFICIEL.find((p) => p.type === "PTA_607");
}

/**
 * Extrait le code département depuis un code postal français.
 * Gère Corse (2A/2B), DOM-TOM (971-976), métropole.
 * @returns null si CP invalide
 */
export function extractDepartmentFromPostalCode(cp: string): string | null {
  if (!cp) return null;
  const cleaned = cp.replace(/\s/g, "");
  if (cleaned.length !== 5 || !/^\d{5}$/.test(cleaned)) return null;

  if (cleaned.startsWith("20")) {
    const num = parseInt(cleaned.slice(2), 10);
    return num <= 199 ? "2A" : "2B";
  }

  if (cleaned.startsWith("97")) return cleaned.slice(0, 3);

  // Métropole : on conserve le code à 2 chiffres tel quel ("01", "75"...)
  // pour matcher les clés de PTA_OFFICIEL.
  return cleaned.slice(0, 2);
}
